package com.research.moodlevalidator.models;

public class TopicHeading {
	private String headingName;
	private String checkHeadingName;
	private String checkheadingDesc;
	public String getHeadingName() {
		return headingName;
	}
	public void setHeadingName(String headingName) {
		this.headingName = headingName;
	}
	public String getCheckHeadingName() {
		return checkHeadingName;
	}
	public void setCheckHeadingName(String checkHeadingName) {
		this.checkHeadingName = checkHeadingName;
	}
	public String getCheckheadingDesc() {
		return checkheadingDesc;
	}
	public void setCheckheadingDesc(String checkheadingDesc) {
		this.checkheadingDesc = checkheadingDesc;
	}
	
	
	
}
